package com.example.androidfunctionality.searchview;

class SearchItem {

    private String text1;
    private String text2;

    SearchItem(String text1, String text2) {
        this.text1 = text1;
        this.text2 = text2;
    }

    String getText1() {
        return text1;
    }

    String getText2() {
        return text2;
    }
}
